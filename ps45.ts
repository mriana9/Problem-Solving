/*
DESCRIPTION:
Very simple, given an integer or a floating-point number, find its opposite.

Examples:

1: -1
14: -14
-34: 34
*/

function opposite(number) {
    return number * -1;
}

console.log(opposite(0));
console.log(opposite(-33.33));
console.log(opposite(5));
console.log(opposite(-8));